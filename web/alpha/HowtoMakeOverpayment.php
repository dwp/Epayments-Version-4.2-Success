</aside><header><h1>3. How to make a repayment</h1>
    </header><p>You’ll be asked to make payments to the Department for Work and Pensions (<abbr title="Department for Work and Pensions">DWP</abbr>) Debt Management organisation if you no longer receive the benefit. You can contact them to arrange a payment plan.</p>
<br/>
	<h3>Make a debit or credit card payment online</h3>
	<p>You can repay an overpayment by debit or credit card.</p>
	<p>You may incur interest charges if you pay by credit card. </p>
	<p>Only use this service if you are paying in full.</p>
	<p> Please <a href="./Overpayment3.htm"><b>click here</b></a> to make an online payment.</p>
<br/>
<h3>Online banking</h3>

<p>Use <abbr title="Department for Work and Pensions">DWP</abbr>’s bank account details to pay, quoting the reference number shown on your letter.</p>

<table>
<thead><tr>
<th>Account name</th>
      <th>Sort code</th>
      <th>Account number</th>
    </tr></thead>
<tbody><tr>
