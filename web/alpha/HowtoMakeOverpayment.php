    </tr></tbody>
</table>
<h3>Direct Debit, cheques and cash</h3>

<p>Contact Debt Management to:  </p>

<ul>
<li>set up monthly repayments by Direct Debit   </li>
  <li>request a paying-in slip for cheque or cash payments  </li>
</ul>
<div class="contact">
<p><strong><abbr title="Department for Work and Pensions">DWP</abbr> Debt Management contact centre</strong><br>
Telephone: 0345 850 0293<br>
Textphone: 0345 604 6697<br>
Calling from abroad: +44 (0)161 904 1233 <br>
Monday to Friday, 8am to 8pm <br>
Saturday, 9am to 4pm<br><a href="/call-charges">Find out about call charges</a></p>
</div>

<h2>If you can’t pay back the money</h2>

<p><abbr title="Department for Work and Pensions">DWP</abbr> may pass your case to an independent debt collector if you haven’t paid back the money you owe.</p>

<p>You’ll get a letter from one of the following suppliers saying that it’s acting on behalf of <abbr title="Department for Work and Pensions">DWP</abbr>:</p>

<ul>
<li>Akinika (formerly iQor) </li>
  <li>CCS Collect </li>
  <li>drydensfairfax solicitors </li>
  <li>Credit Solutions </li>
  <li>Fredricksons </li>
  <li>dlc (Direct Legal &amp; Collections)</li>
</ul>
<p>You should deal directly with the debt collector to arrange repayment.</p>



